    private void btnDesempilharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesempilharActionPerformed
        if (rdoInt.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "Elemento " + pInt.pop() + " desempilhado!");
            txtPilhaAtual.setText(pInt.imprime());
            lblTamanho.setText("A pilha Int possui " + pInt.size() + " elementos.");
        } else if (rdoString.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "Elemento " + pStr.pop() + " desempilhado!");
            txtPilhaAtual.setText(pStr.imprime());
            lblTamanho.setText("A pilha Str possui " + pStr.size() + " elementos.");
        }
    }//GEN-LAST:event_btnDesempilharActionPerformed

