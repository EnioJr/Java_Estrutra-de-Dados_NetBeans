    private void btnEmpilharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmpilharActionPerformed
        if (rdoInt.isSelected()) {
            int valor = Integer.parseInt(txtEmpilhar.getText());
            pInt.push(valor);
            txtPilhaAtual.setText(pInt.imprime());
            lblTamanho.setText("A pilha Int possui " + pInt.size() + " elementos.");
            
        } else if (rdoString.isSelected()) {
            String valor = txtEmpilhar.getText();
            pStr.push(valor);
            txtPilhaAtual.setText(pStr.imprime());
            lblTamanho.setText("A pilha Str possui " + pStr.size() + " elementos.");
        }
        txtEmpilhar.setText("");
        txtEmpilhar.grabFocus();
    }//GEN-LAST:event_btnEmpilharActionPerformed

